package scrum;

public class WorkLoad {
	private Feature feature;
	private Developer developer;
	private int estimatedWorkHours;
	private int workHoursRemaing;
	
	
	public Feature getFeature() {
		return feature;
	}
	public void setFeature(Feature feature) {
		this.feature = feature;
	}
	public Developer getDeveloper() {
		return developer;
	}
	public void setDeveloper(Developer developer) {
		this.developer = developer;
	}
	public int getEstimatedWorkHours() {
		return estimatedWorkHours;
	}
	public void setEstimatedWorkHours(int estimatedWorkHours) {
		this.estimatedWorkHours = estimatedWorkHours;
	}
	public int getWorkHoursRemaing() {
		return workHoursRemaing;
	}
	public void setWorkHoursRemaing(int workHoursRemaing) {
		this.workHoursRemaing = workHoursRemaing;
	}
	
	
	
	

}
